package com.servers.application.json_objects;

public class Joystick {
    private String x;
    private String y;


    // Getter Methods

    public String getX() {
        return x;
    }

    public String getY() {
        return y;
    }

    // Setter Methods

    public void setX(String x) {
        this.x = x;
    }

    public void setY(String y) {
        this.y = y;
    }
}
